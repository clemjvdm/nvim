local opt = vim.opt

-- basics
opt.number = true
opt.tabstop = 4
opt.shiftwidth = 4
opt.expandtab = true

-- search
opt.smartcase = true

-- text rendering
opt.encoding = "utf-8"
opt.linebreak = true -- nicer line wrap
opt.scrolloff = 4 -- minimum 4 lines above/below cursor
opt.sidescrolloff = 5

-- UI
opt.title = true

-- clipboard
vim.opt.clipboard="unnamed,unnamedplus"


































